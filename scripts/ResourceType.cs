/// <summary>
/// Перечисление типов ресурсов в игре
/// </summary>
public enum ResourceType
{
    Metal,      // Металлические руды (железо, титан и т.д.)
    Crystal,    // Кристаллы (энергетические кристаллы, минералы)
    Organic,    // Органические материалы (биомасса, растения)
    Energy,     // Энергетические источники
    Composite   // Композитные материалы (сплавы, смеси)
}